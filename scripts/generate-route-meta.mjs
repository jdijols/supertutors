#!/usr/bin/env node
/**
 * generate-route-meta.mjs — postbuild step that emits route-specific
 * HTML files with custom title / description / OpenGraph / Twitter meta
 * tags, so link unfurlers (iMessage, Slack, Discord, Twitter, Facebook,
 * etc.) see the right preview for each route in this SPA.
 *
 * Why this exists: every non-/api route falls through to the SPA rewrite
 * in vercel.json (→ /index.html). Crawlers don't run React, so a dynamic
 * `document.title` doesn't affect link previews. To get a per-route
 * preview we have to ship a real HTML file at that path. Vercel serves
 * static files before applying rewrites, so `dist/lessons/acutis/index.html`
 * wins over the fallback for that URL — but the bundled React app still
 * boots and routes correctly because the bundle is identical.
 *
 * To add a new route override, append to ROUTES below. The script will
 * fail loudly if the marker block in index.html drifts.
 */
import { readFile, writeFile, mkdir } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.resolve(__dirname, "..");
const DIST_DIR = path.join(REPO_ROOT, "dist");
const TEMPLATE_PATH = path.join(DIST_DIR, "index.html");

const SITE = "https://supertutors.vercel.app";
const DEFAULT_IMAGE = `${SITE}/og-image.png`;
const DEFAULT_IMAGE_ALT =
  "Saint Carlo Acutis Institute — research brief on a Catholic vertical for Superbuilders.";

/** Routes that need their own meta. Path is the URL path; the script emits
 *  `dist{path}/index.html` for each. Add more entries as lessons get share-worthy. */
const ROUTES = [
  {
    path: "/lessons/acutis",
    title: "St. Carlo Acutis Institute - Catholic AlphaSchool",
    description:
      "A research brief on a Catholic vertical for Superbuilders, named for the patron saint of the internet.",
    image: DEFAULT_IMAGE,
    imageAlt:
      "Saint Carlo Acutis Institute — Catholic AlphaSchool research brief. Portrait of Carlo Acutis on a warm off-white card.",
  },
];

const MARKER_START = "<!-- META_BLOCK_START";
const MARKER_END = "<!-- META_BLOCK_END -->";

function escapeAttr(s) {
  return s
    .replace(/&/g, "&amp;")
    .replace(/"/g, "&quot;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;");
}

function renderMetaBlock(route, url) {
  const t = escapeAttr(route.title);
  const d = escapeAttr(route.description);
  const img = escapeAttr(route.image);
  const alt = escapeAttr(route.imageAlt);
  const u = escapeAttr(url);
  return [
    `<!-- META_BLOCK_START — generated by scripts/generate-route-meta.mjs for ${route.path} -->`,
    `    <title>${t}</title>`,
    `    <meta name="description" content="${d}" />`,
    `    <meta property="og:type" content="website" />`,
    `    <meta property="og:site_name" content="SuperTutors" />`,
    `    <meta property="og:url" content="${u}" />`,
    `    <meta property="og:title" content="${t}" />`,
    `    <meta property="og:description" content="${d}" />`,
    `    <meta property="og:image" content="${img}" />`,
    `    <meta property="og:image:type" content="image/png" />`,
    `    <meta property="og:image:width" content="1200" />`,
    `    <meta property="og:image:height" content="630" />`,
    `    <meta property="og:image:alt" content="${alt}" />`,
    `    <meta name="twitter:card" content="summary_large_image" />`,
    `    <meta name="twitter:title" content="${t}" />`,
    `    <meta name="twitter:description" content="${d}" />`,
    `    <meta name="twitter:image" content="${img}" />`,
    `    <meta name="twitter:image:alt" content="${alt}" />`,
    `    <!-- META_BLOCK_END -->`,
  ].join("\n");
}

function replaceMetaBlock(html, route) {
  const startIdx = html.indexOf(MARKER_START);
  const endIdx = html.indexOf(MARKER_END);
  if (startIdx === -1 || endIdx === -1) {
    throw new Error(
      `Could not find META_BLOCK markers in dist/index.html. Did index.html drift? Expected '${MARKER_START}…' and '${MARKER_END}'.`,
    );
  }
  // Walk backward from startIdx to include the leading whitespace on that line so
  // we replace the whole block cleanly (the marker comment is indented in source).
  let blockStart = startIdx;
  while (blockStart > 0 && (html[blockStart - 1] === " " || html[blockStart - 1] === "\t")) {
    blockStart--;
  }
  const blockEnd = endIdx + MARKER_END.length;
  const url = `${SITE}${route.path}`;
  return html.slice(0, blockStart) + renderMetaBlock(route, url) + html.slice(blockEnd);
}

async function main() {
  const template = await readFile(TEMPLATE_PATH, "utf8");

  for (const route of ROUTES) {
    const html = replaceMetaBlock(template, route);
    const outDir = path.join(DIST_DIR, route.path.replace(/^\//, ""));
    const outFile = path.join(outDir, "index.html");
    await mkdir(outDir, { recursive: true });
    await writeFile(outFile, html, "utf8");
    console.log(`✓ wrote ${path.relative(REPO_ROOT, outFile)}  →  "${route.title}"`);
  }
}

main().catch((err) => {
  console.error("✗ generate-route-meta failed:", err.message);
  process.exit(1);
});
